void SEG7_set(int index, alt_u32 seg_mask){
	alt_u32 value;
	value = IORD(PIO_SEG7_BASE, 0);
	seg_mask^=0xff;
	seg_mask<<=8*index;
	value &= seg7_mask[index];
	value |= seg_mask;
	IOWR(PIO_SEG7_BASE, 0, value);
}

